package controller

import (
	"net/http"

	"github.com/QuantumNous/new-api/common"
	"github.com/QuantumNous/new-api/i18n"
	"github.com/QuantumNous/new-api/model"

	"github.com/gin-gonic/gin"
)

// GetOrCreateAccessToken returns the caller's existing access token, or
// generates a new one if none exists. Unlike GenerateAccessToken, this
// handler does NOT replace an existing token, so it can be invoked
// repeatedly (e.g. by the CC Switch deep-link dialog) without disturbing
// other clients that already hold the token.
func GetOrCreateAccessToken(c *gin.Context) {
	id := c.GetInt("id")
	user, err := model.GetUserById(id, true)
	if err != nil {
		common.ApiError(c, err)
		return
	}

	if user.GetAccessToken() != "" {
		c.JSON(http.StatusOK, gin.H{
			"success": true,
			"message": "",
			"data":    user.GetAccessToken(),
		})
		return
	}

	randI := common.GetRandomInt(4)
	key, err := common.GenerateRandomKey(29 + randI)
	if err != nil {
		common.ApiErrorI18n(c, i18n.MsgGenerateFailed)
		common.SysLog("failed to generate key: " + err.Error())
		return
	}
	user.SetAccessToken(key)

	if model.DB.Where("access_token = ?", user.AccessToken).First(user).RowsAffected != 0 {
		common.ApiErrorI18n(c, i18n.MsgUuidDuplicate)
		return
	}

	if err := user.Update(false); err != nil {
		common.ApiError(c, err)
		return
	}

	c.JSON(http.StatusOK, gin.H{
		"success": true,
		"message": "",
		"data":    user.AccessToken,
	})
}
